import httpx
import json
import time

API_URL = "http://127.0.0.1:8000/eval/run-all"
OUTPUT_FILE = "EVAL_REPORT.md"

def generate_report():
    print("🚀 Triggering automated evaluation suite... This will take ~60 seconds.")
    print("⚠️ Please ensure your local FastAPI server is running (python run.py)!")
    
    try:
        # We use a very long timeout since evaluating 12 cases takes time
        with httpx.Client(timeout=180.0) as client:
            response = client.post(API_URL)
            response.raise_for_status()
            data = response.json()
    except Exception as e:
        print(f"\n❌ Error: Failed to reach the API. Is your server running on {API_URL}?")
        print(f"Details: {e}")
        return

    print("✅ Evaluation complete! Generating Markdown report...")
    
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        f.write("# Plum AI Claims - Automated Evaluation Report\n\n")
        f.write("This report was automatically generated by executing the 12 system test cases through the multi-agent pipeline.\n\n")
        
        # Summary Section
        summary = data.get("summary", {})
        f.write("## 📊 Executive Summary\n\n")
        f.write(f"- **Total Cases Run:** {summary.get('total')}\n")
        f.write(f"- **Passed:** {summary.get('passed')} ✅\n")
        f.write(f"- **Failed:** {summary.get('failed')} ❌\n\n")
        
        f.write("---\n\n")
        f.write("## 📝 Detailed Case Results\n\n")
        
        results = data.get("results", [])
        for tc in results:
            pass_status = "✅ PASS" if tc.get("status") == "PASS" else "❌ FAIL"
            f.write(f"### {tc.get('case_id')}: {tc.get('case_name')} [{pass_status}]\n\n")
            f.write(f"**Description:** {tc.get('description')}\n\n")
            
            # Expected vs Actual
            expected_dec = tc.get("expected_decision", "N/A")
            actual_dec = tc.get("actual_decision", "ERROR")
            
            f.write("#### Outcome Comparison\n")
            f.write(f"- **Expected Decision:** `{expected_dec}`\n")
            f.write(f"- **Actual System Decision:** `{actual_dec}`\n\n")
            
            if tc.get("status") != "PASS":
                f.write("> **Mismatch Explanation:** The system's actual decision did not match the strictly expected outcome defined in the test case. Please review the trace below to identify the agent logic divergence.\n\n")
            
            # Trace Breakdown
            f.write("#### 🔍 Pipeline Trace Breakdown\n\n")
            trace_summary = tc.get("trace_summary", {})
            f.write(f"**Final Confidence Score:** {(trace_summary.get('confidence', 0) * 100):.0f}%\n\n")
            
            f.write("| Step | Agent | Action | Result |\n")
            f.write("|------|-------|--------|--------|\n")
            
            steps = trace_summary.get("agent_steps", [])
            for i, step in enumerate(steps):
                agent_name = step.get("agent", "Unknown").replace("_", " ").title()
                action = step.get("action", "").replace("\n", " ")
                result = step.get("result", "").replace("\n", " ")
                f.write(f"| {i+1} | **{agent_name}** | {action} | {result} |\n")
            
            f.write("\n---\n\n")
            
    print(f"\n🎉 Success! The detailed evaluation report has been saved to: {OUTPUT_FILE}")

if __name__ == "__main__":
    generate_report()
